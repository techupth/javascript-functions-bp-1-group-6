// Function Expression
function add(a,b){
    return a+b;
};
console.log(`${add(10,5)}`); //15 //a=10,b=5

function subtract(a,b){
    return a-b;
};
console.log(`${subtract(10,5)}`);//5 //a=10,b=5

function multiply(a,b){
    return a*b;
};
console.log(`${multiply(10,5)}`);//50 //a=10,b=5

function divide(a,b){
    return a/b;
}
console.log(`${divide(10,5)}`);//2 //a=10,b=5