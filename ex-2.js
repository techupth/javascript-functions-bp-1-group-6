// Start coding here
// Function Expression สำหรับการบวก
let add = function (a, b) {
    return a + b;
};

// Function Expression สำหรับการลบ
let subtract = function (a, b) {
    return a - b;
};

// Function Expression สำหรับการคูณ
let multiply = function (a, b) {
    return a * b;
};

// Function Expression สำหรับการหาร
let divide = function (a, b) {
    return a / b;
};

console.log(add(10, 5));      // ผลลัพธ์: 15
console.log(subtract(10, 5)); // ผลลัพธ์: 5
console.log(multiply(10, 5)); // ผลลัพธ์: 50
console.log(divide(10, 5));   // ผลลัพธ์: 2
