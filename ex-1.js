// Function Declaration
function add(a,b){
    return a+b;
};
console.log(`${add}`);

function subtract(a,b){
    return a-b;
};
console.log(`${subtract}`);

function multiply(a,b){
    return a*b;
};
console.log(`${multiply}`);

function divide(a,b){
    return a/b;
};
console.log(`${divide}`);