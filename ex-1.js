// Function Declaration สำหรับการบวก
function add(a, b) {
    return a + b;
}

// Function Declaration สำหรับการลบ
function subtract(a, b) {
    return a - b;
}

// Function Declaration สำหรับการคูณ
function multiply(a, b) {
    return a * b;
}

// Function Declaration สำหรับการหาร
function divide(a, b) {
    return a / b;
}

console.log(add(10, 5));      // ผลลัพธ์: 15
console.log(subtract(10, 5)); // ผลลัพธ์: 5
console.log(multiply(10, 5)); // ผลลัพธ์: 50
console.log(divide(10, 5));   // ผลลัพธ์: 2
