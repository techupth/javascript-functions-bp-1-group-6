// Exercise #3 Declaring Arrow Function
// Start coding here
// Arrow Function แบบย่อสำหรับการบวก
let add = (a, b) => a + b;
console.log(add(10, 5)); // ผลลัพธ์: 15

// Arrow Function แบบย่อสำหรับการลบ
let subtract = (a, b) => a - b;
console.log(subtract(10, 5)); // ผลลัพธ์: 5

// Arrow Function แบบย่อสำหรับการคูณ
let multiply = (a, b) => a * b;
console.log(multiply(10, 5)); // ผลลัพธ์: 50

// Arrow Function แบบย่อสำหรับการหาร
const divide = (a, b) => a / b;
console.log(divide(10, 5)); // ผลลัพธ์: 2
