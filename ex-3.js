let add = (a,b) => {                   // Arrow Function แบบปกติ
    return a + b;
}

// subtract
let subtract = (a,b) => {               // Arrow Function แบบปกติ
    return a - b;
}

// multiply
let multiply = (a,b) => a * b;

// divide
let divide = (a,b) => a / b;